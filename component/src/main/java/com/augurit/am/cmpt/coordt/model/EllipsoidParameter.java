package com.augurit.am.cmpt.coordt.model;

/**
 * 坐标系的椭球定义
 */
public class EllipsoidParameter {
	private double a;//长半轴
	private double f;//扁 率

	/**
	 * 返回长半轴
	 * @return 长半轴
     */
	public double getA() {
		return a;
	}

	/**
	 * 设置长半轴
	 * @param a 长半轴
     */
	public void setA(double a) {
		this.a = a;
	}

	/**
	 * 返回扁率
	 * @return 扁率
     */
	public double getF() {
		return f;
	}

	/**
	 * 设置扁率
	 * @param f 扁率
     */
	public void setF(double f) {
		this.f = f;
	}

}
