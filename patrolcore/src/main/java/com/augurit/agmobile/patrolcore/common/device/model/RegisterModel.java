public class RegisterModel {
    private String device; //设备唯一ID
    private String id;//注册服务器产生的ID
    private String userId;//当前注册用户的用户ID

    public String getDevice() {
        return device;
    }

    public void setDevice(String device) {
        this.device = device;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }
}
