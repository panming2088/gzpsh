package com.augurit.agmobile.patrolcore.survey.model;

import java.io.Serializable;

/**
 * 基本人口信息，主要用于从套表或者人口基本表中带数据进行填充流动人口和境外人口的信息
 *
 * @author 创建人 ：xuciluan
 * @version 1.0
 * @package 包名 ：com.augurit.agsurvey.shiyourenkou.model
 * @createTime 创建时间 ：17/9/12
 * @modifyBy 修改人 ：xuciluan
 * @modifyTime 修改时间 ：17/9/12
 * @modifyMemo 修改备注：
 */

public class BasicRenKouInfo implements Serializable{

    private String address ;//标准地址或者在华地址

    private String name ;//姓名

    private String idenitfyNo;//证件号码

    private String telephone ;//本人电话号码

    private String gender; //性别

    private String apartmentNo ;//出租屋编号

    private String houseOwner ;//屋主

    private String houserOwnerPhone ;//屋主电话

    private String houseOwnerIdentifyId ;//屋主证件号

    private String houseType ; //房屋类型

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getIdenitfyNo() {
        return idenitfyNo;
    }

    public void setIdenitfyNo(String idenitfyNo) {
        this.idenitfyNo = idenitfyNo;
    }

    public String getTelephone() {
        return telephone;
    }

    public void setTelephone(String telephone) {
        this.telephone = telephone;
    }


    public String getApartmentNo() {
        return apartmentNo;
    }

    public void setApartmentNo(String apartmentNo) {
        this.apartmentNo = apartmentNo;
    }

    public String getHouseOwner() {
        return houseOwner;
    }

    public void setHouseOwner(String houseOwner) {
        this.houseOwner = houseOwner;
    }

    public String getHouseOwnerPhone() {
        return houserOwnerPhone;
    }

    public void setHouserOwnerPhone(String houserOwnerPhone) {
        this.houserOwnerPhone = houserOwnerPhone;
    }

    public String getHouseOwnerIdentifyId() {
        return houseOwnerIdentifyId;
    }

    public void setHouseOwnerIdentifyId(String houseOwnerIdentifyId) {
        this.houseOwnerIdentifyId = houseOwnerIdentifyId;
    }

    public String getHouseType() {
        return houseType;
    }

    public void setHouseType(String houseType) {
        this.houseType = houseType;
    }
}
