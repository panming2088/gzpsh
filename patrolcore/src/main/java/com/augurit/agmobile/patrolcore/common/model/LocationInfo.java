package com.augurit.agmobile.patrolcore.common.model;

import android.location.Location;

import com.augurit.agmobile.patrolcore.selectlocation.model.DetailAddress;
import com.esri.core.geometry.Point;

/**
 * 位置信息
 * Created by xcl on 2017/10/27.
 */

public class LocationInfo {
    /**
     * 转换后的地图坐标
     */
    private Point point;

    /**
     * 没有转换前的原始坐标
     */
    private Location location;

    /**
     * 当前位置的具体地址
     */
    private DetailAddress detailAddress;

    public Point getPoint() {
        return point;
    }

    public void setPoint(Point point) {
        this.point = point;
    }

    public Location getLocation() {
        return location;
    }

    public void setLocation(Location location) {
        this.location = location;
    }

    public DetailAddress getDetailAddress() {
        return detailAddress;
    }

    public void setDetailAddress(DetailAddress detailAddress) {
        this.detailAddress = detailAddress;
    }
}
