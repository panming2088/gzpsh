    /**
     * 截取『市』之后的地址
     *
     * @param context
     * @return
     */
    public String getAddressWithoutCity(Context context) {
        int provinceLocation = getDetailAddress().indexOf("市");
        String formattedAddress = getDetailAddress().substring(provinceLocation + 1);
        formattedAddress = context.getResources().getString(R.string.the_current_address_is_near, formattedAddress);
        return formattedAddress;
    }

    /**
     * 获取详细的地址描述
     *
     * @return
     */
    public String getDetailAddress() {
        if (getResult() != null) {
            return getResult().getFormatted_address() + getResult().getSematic_description();
        }
        return null;
    }
