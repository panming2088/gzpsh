public class LineOverlay extends Overlay {


    protected int lineWidth;
    protected int lineColor;

    private LineOverlay(){}

    public LineOverlay lineWith(int lineWith){
        this.lineWidth = lineWith;
        return this;
    }

    public LineOverlay lineColor(int lineColor){
        this.lineColor = lineColor;
        return this;
    }

    public LineOverlay getThis() {
        return this;
    }

}
