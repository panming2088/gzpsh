package com.augurit.agmobile.mapengine.common.agmobilelayer.model;


import com.esri.core.geometry.Point;

import java.util.List;

/**
 * Created by f on 2016-06-30.
 */
public class CapabilitiesBean {
    private String layer;
    private Point lowerCornerPoint;
    private Point upperCornerPoint;
    private String tileMatrixSet;
    private String format;
    private String style;
    private String serviceMode;
    private String spatialReference;
    private int minZoomLevel;
    private int maxZoomLevel;
    private List<Double> scales;
    private Point topLeftCorner;
    private String version;

    public String getLayer() {
        return layer;
    }

    public void setLayer(String layer) {
        this.layer = layer;
    }

    public Point getLowerCornerPoint() {
        return lowerCornerPoint;
    }

    public void setLowerCornerPoint(Point lowerCornerPoint) {
        this.lowerCornerPoint = lowerCornerPoint;
    }

    public Point getUpperCornerPoint() {
        return upperCornerPoint;
    }

    public void setUpperCornerPoint(Point upperCornerPoint) {
        this.upperCornerPoint = upperCornerPoint;
    }

    public String getTileMatrixSet() {
        return tileMatrixSet;
    }

    public void setTileMatrixSet(String tileMatrixSet) {
        this.tileMatrixSet = tileMatrixSet;
    }

    public String getFormat() {
        return format;
    }

    public void setFormat(String format) {
        this.format = format;
    }

    public String getStyle() {
        return style;
    }

    public void setStyle(String style) {
        this.style = style;
    }

    public String getServiceMode() {
        return serviceMode;
    }

    public void setServiceMode(String serviceMode) {
        this.serviceMode = serviceMode;
    }

    public String getSpatialReference() {
        return spatialReference;
    }

    public void setSpatialReference(String spatialReference) {
        this.spatialReference = spatialReference;
    }

    public int getMinZoomLevel() {
        return minZoomLevel;
    }

    public void setMinZoomLevel(int minZoomLevel) {
        this.minZoomLevel = minZoomLevel;
    }

    public int getMaxZoomLevel() {
        return maxZoomLevel;
    }

    public void setMaxZoomLevel(int maxZoomLevel) {
        this.maxZoomLevel = maxZoomLevel;
    }

    public List<Double> getScales() {
        return scales;
    }

    public void setScales(List<Double> scales) {
        this.scales = scales;
    }

    public Point getTopLeftCorner() {
        return topLeftCorner;
    }

    public void setTopLeftCorner(Point topLeftCorner) {
        this.topLeftCorner = topLeftCorner;
    }

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }
}
